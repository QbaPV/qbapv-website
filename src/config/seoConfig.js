// Función para obtener meta tags por página e idioma
export const getPageSEO = (page, language = 'es') => {
  const pageData = seoConfig.pages[page];
  if (!pageData) return null;
  
  return {
    title: pageData.title[language] || pageData.title.es,
    description: pageData.description[language] || pageData.description.es,
    keywords: pageData.keywords[language] || pageData.keywords.es,
    locale: seoConfig.openGraph.locale[language] || seoConfig.openGraph.locale.es
  };
};

// Función para generar URL completa
export const getCanonicalUrl = (path = '') => {
  return `${seoConfig.siteUrl}${path}`;
};

